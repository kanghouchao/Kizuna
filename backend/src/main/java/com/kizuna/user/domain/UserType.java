package com.kizuna.user.domain;

/**
 * プラットフォームユーザーの本人種別（#382 / #398）。授権モデルの対象は STAFF のみで、CAST / MEMBER は能力モデルに入らない独立した本人種別として扱う（#320
 * 既定）。
 */
public enum UserType {
  /** 社内利用者。能力束 × 担当店舗集合 ×（必要時）精算範囲で授権される。 */
  STAFF,
  /** キャスト本人。店舗招待制で紐づき、本人ポータルのみ利用する。 */
  CAST,
  /** 会員本人。本人のデータのみ利用する。 */
  MEMBER
}
